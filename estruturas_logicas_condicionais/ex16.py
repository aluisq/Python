
num = int(input('Digite um número de 1 a 12: '))

if 1 <= num <= 12:
    if num == 1:
        print('Janeiro')
    if num == 2:
        print('Fevereiro')
    if num == 3:
        print('Março')
    if num == 4:
        print('Abril')
    if num == 5:
        print('Maio')
    if num == 6:
        print('Junho')
    if num == 7:
        print('Julho')
    if num == 8:
        print('Agosto')
    if num == 9:
        print('Setembro')
    if num == 10:
        print('Outubro')
    if num == 11:
        print('Novembro')
    if num == 12:
        print('Dezembro')
else:
    print('Número inválido')